function abrir(){
    document.getElementById('esmaecer').style.visibility = 'visible';
    document.getElementById('menu').style.visibility = 'visible';
    document.getElementById('x').style.visibility = 'visible';
    document.getElementById('sanduiche').style.visibility = 'hidden';
}
function fechar(){
    document.getElementById('esmaecer').style.visibility = 'hidden';
    document.getElementById('menu').style.visibility = 'hidden';
    document.getElementById('x').style.visibility = 'hidden';
    document.getElementById('sanduiche').style.visibility = 'visible';
}